# Step 6. 잘못된 예시 → SyntaxError 때문에 실행조차 안 됨
print("잘못된 예시: f = open(\"C:\\Users\\Guest\\test_path.txt\")")
print("→ SyntaxError 발생 (파이썬이 \\U 를 유니코드 이스케이프 시퀀스로 인식)")

# Step 7. 해결 방법 1: raw string 사용
try:
    f = open(r"C:\Users\Guest\test_path.txt", "r", encoding="utf-8")
    print("[raw string 읽기 결과]")
    print(f.read())
    f.close()
except Exception as e:
    print("[raw string 실행 오류]", e)

# Step 8. 해결 방법 2: 백슬래시 두 번 사용
